nilai_a = float(input("Masukkan Nilai Pertama: "))
nilai_b = float(input("Masukkan Nilai Kedua: "))
nilai_i = float(input("Masukkan Nilai Ketiga: "))
nilai_j = float(input("Masukkan Nilai Ke-empat: "))
nilai_x = float(input("Masukkan Nilai Kelima: "))
nilai_y = float(input("Masukkan Nilai Ke-enam: "))

hasil = (nilai_a - nilai_b) * (nilai_i / nilai_j) - (nilai_x + nilai_y)

print(f"Hasil {hasil:.3f}")