a = 4
b = 8
c = 3

print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

sama_a_b = a == b
lebih_besar_b_c = b > c
tidak_sama_a_c = a != c

print(f"Apakah a sama dengan b? jawabannya adalah {int(sama_a_b)}")
print(f"Apakah b lebih besar dari c? jawabannya adalah {int(lebih_besar_b_c)}")
print(f"Apakah a tidak sama dengan c? jawabannya adalah {int(tidak_sama_a_c)}")